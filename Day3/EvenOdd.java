package Day3;

public class EvenOdd {
    public static void main(String[] args) {
        for(int i=1;i<=40;i++){
            if (i%2 ==0) {
                System.out.println("Number is Even:"+i);
            }else{
                System.out.println("Number is Odd:"+i);
            }
        }
    }
}
